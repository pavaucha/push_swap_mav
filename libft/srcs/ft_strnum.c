#include <libft.h>

int		ft_strnum(char *str)
{
	int		i;

	i = 0;
	if (str[0] == '-' || str[0] == '+')
	{
		i++;
		if (!str[i])
			return (0);
	}
	while (str[i])
	{
		if (!ft_isdigit(str[i]))
			return (0);
		i++;
	}
	return (1);
}
