#include <libft.h>

void	ft_strdel_tab(char ***as)
{
	int		i;

	if (as == NULL)
		return ;
	else
	{
		i = -1;
		while ((*as)[++i])
		{
			free((*as)[i]);
			(*as)[i] = NULL;
		}
	}
	free(*as);
	*as = NULL;
}
