#include <libft.h>

void	*ft_memalloc(size_t size)
{
	char	*str;
	size_t	i;

	i = 0;
	if (!(str = (char*)malloc(sizeof(char) * (size + 1))))
		return (NULL);
	if (str == NULL)
		return (NULL);
	while (i < size)
	{
		*(str + i) = '\0';
		i++;
	}
	return (str);
}
